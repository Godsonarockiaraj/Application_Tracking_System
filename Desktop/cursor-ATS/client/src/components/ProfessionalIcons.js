// Professional icon mappings for dashboard stats
export const getStatIcon = (type) => {
  switch (type) {
    case 'pending':
      return <ClockIcon />;
    case 'shortlisted':
      return <CheckIcon />;
    case 'rejected':
      return <XIcon />;
    case 'success':
      return <BarChartIcon />;
    default:
      return <DashboardIcon />;
  }
};

// Professional role icons
export const getRoleIcon = (role) => {
  switch (role) {
    case 'applicant':
      return <UserIcon />;
    case 'admin':
      return <SettingsIcon />;
    case 'bot_mimic':
      return <BotIcon />;
    default:
      return <UserIcon />;
  }
};